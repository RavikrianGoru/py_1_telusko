print('2 << 3 =', 2 << 3) # left shift
print(f'2 << 3 : {bin(2)}  means {bin(2<<3)}')
print('-2 << 3 =', -2 << 3) # left shift
print(f'-2 << 3 : {bin(-2)}  means {bin(-2<<3)}')

print('11 >> 2 =', 11 >> 2) # right shift
print(f'11 >> 2 : {bin(11)}  means {bin(11>>2)}')
print('-11 >> 2 =', -11 >> 2) # right shift
print(f'-11 >> 2 : {bin(-11)}  means {bin(-11>>2)}')

